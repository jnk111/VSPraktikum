package vs.malte.json;

public class NewAccountDTO
{
    private String player;
    private int saldo;
    
    public NewAccountDTO()
    {
    }

    public String getPlayer()
    {
        return player;
    }

    public void setPlayer( String player )
    {
        this.player = player;
    }

    public int getSaldo()
    {
        return saldo;
    }

    public void setSaldo( int saldo )
    {
        this.saldo = saldo;
    }
    
    
}
